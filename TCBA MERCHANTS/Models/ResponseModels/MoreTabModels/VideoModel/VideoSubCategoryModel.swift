import Foundation

struct VideoSubCategoryModel: Codable {
    let videos: [VideoSubCategoryVideo]?
    let subcategories: [JSONAny]?
}

struct VideoSubCategoryVideo: Codable {
    let videoID: Int?
    let videoLink: String?
    let videoDescription, videoTitle, videoCreated, videoModified: String?
    let videoImage: String?
    
    enum CodingKeys: String, CodingKey {
        case videoID = "videoId"
        case videoLink, videoDescription, videoTitle, videoCreated, videoModified, videoImage
    }
}

// MARK: Convenience initializers and mutators

extension VideoSubCategoryModel {
    init(data: Data) throws {
        self = try newJSONDecoder().decode(VideoSubCategoryModel.self, from: data)
    }
    
    init(_ json: String, using encoding: String.Encoding = .utf8) throws {
        guard let data = json.data(using: encoding) else {
            throw NSError(domain: "JSONDecoding", code: 0, userInfo: nil)
        }
        try self.init(data: data)
    }
    
    init(fromURL url: URL) throws {
        try self.init(data: try Data(contentsOf: url))
    }
    
    func with(
        videos: [VideoSubCategoryVideo]?? = nil,
        subcategories: [JSONAny]?? = nil
        ) -> VideoSubCategoryModel {
        return VideoSubCategoryModel(
            videos: videos ?? self.videos,
            subcategories: subcategories ?? self.subcategories
        )
    }
    
    func jsonData() throws -> Data {
        return try newJSONEncoder().encode(self)
    }
    
    func jsonString(encoding: String.Encoding = .utf8) throws -> String? {
        return String(data: try self.jsonData(), encoding: encoding)
    }
}

